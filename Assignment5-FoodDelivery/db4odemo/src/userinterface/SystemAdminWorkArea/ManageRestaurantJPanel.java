    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int selectedrow = enterpriseJTable.getSelectedRow();
        boolean flag = false;
        Restaurant selectedRestaurant = null;
        Employee emp = null;
        if (selectedrow >= 0) {
            Restaurant restaurantName = (Restaurant) enterpriseJTable.getValueAt(selectedrow, 0);
            for (Restaurant rest : system.getRestaurantDirectory().getRestaurantList()) {
                if (rest != null && rest.getRestaurantAdminUsername()!= null) {
                    if (rest.getRestaurantAdminUsername().equals(restaurantName.getRestaurantAdminUsername())) {
                        flag = true;
                        selectedRestaurant = rest;
                        break;
                    }
                }
            }
            if (flag) {
                boolean userAccountFlag = false;
                UserAccount _userAccount = null;
                for (Employee employee : selectedRestaurant.getEmployeeDirectory().getEmployeeList()) {
                    for (UserAccount ua : system.getUserAccountDirectory().getUserAccountList()) {
                        if (ua.getUsername().equals(employee.getName())) {
                            userAccountFlag = true;
                            _userAccount = ua;
                            break;
                        }
                    }
                }
                if (userAccountFlag) {
                    system.getUserAccountDirectory().deleteUserAccount(_userAccount);
                    system.getRestaurantDirectory().deleteRestaurant(selectedRestaurant);
                }
                // system.getUserAccountDirectory().deleteUserAccount(userAccount);
                JOptionPane.showMessageDialog(null, "Row Deleted Successfully!!");
                populateTable();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please Select a Row");
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed

        //   Enterprise enterprise = (Enterprise) enterpriseJComboBox.getSelectedItem();
        String username = usernameJTextField.getText();
        String address = addressJField.getText();
        String password = String.valueOf(passwordJPasswordField.getPassword());
        String name = nameJTextField.getText();
        String phone = phoneJTxt.getText();
        String email = emailJTxt.getText();

        if (username.equals("") || address.equals("") || password.equals("") || name.equals("") || phone.equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter all details");
            return;
        }

        if (validEmail() == false) {
            JOptionPane.showMessageDialog(null, "Please enter correct email id");
            return;
        }

        if (system.checkIfUserIsUnique(username)) {
            Restaurant restaurant = system.createAndAddRestaurant(name, address, phone, email,username);
            Employee employee = restaurant.getEmployeeDirectory().createEmployee(username);
            system.getEmployeeDirectory().createEmployee(name);
            UserAccount account = system.getUserAccountDirectory().createUserAccount(username, password, employee, new AdminRole());
            JOptionPane.showMessageDialog(null, "Restaurant created successfully");
        } else {
            JOptionPane.showMessageDialog(null, "Username already exists");
        }
        populateTable();
        usernameJTextField.setText("");
        nameJTextField.setText("");
        passwordJPasswordField.setText("");
        phoneJTxt.setText("");
        addressJField.setText("");
        emailJTxt.setText("");
    }//GEN-LAST:event_submitJButtonActionPerformed

