    private boolean validEmail() {
       Pattern p = Pattern.compile("^[\\w-_\\.+]*[\\w-_\\.]\\@([\\w]+\\.)+[\\w]+[\\w]$");
        Matcher m = p.matcher(emailJTxt.getText());
        Boolean b = m.matches();
        return b;
    }
